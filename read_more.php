    <h1>De ce Sakura School?</h1>
    <p>Doriţi specializare în IT şi titluri oficiale pe care să le puteţi valorifica? 
Alegeţi domeniul care vă atrage cel mai mult. În faţa dumneavoastră se găsesc şase mari departamente împărțite în 26 de programe. Puteți alege de la programare clasică, programare web, design, administrarea reţelelor, IT business, CAD şi până la dezvoltarea aplicaţiilor mobile.
Pentru fiecare domeniu pe care îl alegeţi, avem două scopuri simple pentru dumneavoastră.
Primul este ca după şcolarizare să avansaţi şi să obţineţi un loc de muncă bine plătit. Al doilea scop este să vă oferim o experienţă educaţională de care vă veţi aduce aminte cu plăcere.
Noi credem în aceste obiective şi ştim cum să le atingem.
De aceea, şcolarizarea se desfăşoară în conformitate cu o programă avansată în IT, autorizată de Departamentul de Examinări Internaţionale Cambridge şi de două corporaţii lider în software. Este compactă, durează 12 luni și este împărţită în două semestre
Cunoştinţele şi aptitudinile necesare pentru utilizarea avansată a calculatorului sunt predate în cadrul a 87 de unităţi educaţionale, cu un fond total de 386 de ore. Datorită caracterului intensiv, veţi reuşi ca într-un singur an să obţineţi o înaltă specializare în lucrul cu tehnologiile IT.
În funcţie de departamentul pentru care ați optat, în urma promovării examenului, veţi obţine titluri de specialitate oficiale, emise de Cambridge, Microsoft şi Adobe, precum şi de alte asociaţii de specialitate din domeniile respective. </p><br><br>
    <h1>Specializarea</h1>
    <p>Această categorie reprezintă o continuare a studiilor specifice pentru absolvenții de la programele de pregătire sau pentru persoanele care sunt deja în IT și doresc să își îmbogățească cunoștințele. Absolvenții noștri sunt admiși în mod automat la oricare dintre cursurile de pe acest nivel. 
        Adresat persoanelor care lucrează deja în industria IT și care își doresc o specializare pe tehnologiile pe care le oferim 
        Aceasta este categoria potrivită pentru cei care au început deja să studieze și sunt acomodați cu noțiunile specifice de bază ale unor discipline IT. În funcție de programul ales, aceste programe impun pre-existența unor concepte de bază precum:  
        programare orientată pe obiecte, algoritmică, tipuri și metode de testare software, arhitectura de bază a aplicațiilor etc</p>
